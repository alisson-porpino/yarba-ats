
const LANGUAGE_ALIASES = {
  en: 'en',
  es: 'es',
  pt: 'pt_br',
  br: 'pt_br',
  'pt-br': 'pt_br',
  pt_br: 'pt_br',
}

export function normalizeLanguageCode(code, fallback = 'en') {
  if (typeof code !== 'string') return fallback
  const normalized = code.trim().toLowerCase()
  return LANGUAGE_ALIASES[normalized] || fallback
}

